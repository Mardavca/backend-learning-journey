// los bucles sirven para repetir un bloque de codigo varias veces, lo que permite automatizar tareas y reducir la cantidad de codigo necesario para realizar una tarea.
// hay 3 tipos de bucles: for, while y do-while. En este caso, se usara el bucle for para iterar sobre un rango de valores y ejecutar un bloque de codigo para cada valor.

//for (inicio; condición; incremento) {
// código que se repite
//} ejemplo: for (let i = 0; i < 5; i++) { console.log(i); } // Output: 0, 1, 2, 3, 4
console.log("Ejemplo 1: Bucle for");

for (let i = 0; i <= 5; i++) {
  console.log("Iteration: " + i);
}
// Output: Iteration: 0, Iteration: 1, Iteration: 2, Iteration: 3, Iteration: 4
// BASICO, declarando variables y usando un bucle for para iterar sobre un rango de valores.
